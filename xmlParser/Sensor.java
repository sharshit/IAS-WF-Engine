package xmlParser;

public class Sensor {
	String sensorId;
	String sensorName;
	String sensorType;
	String sensorJarName;
	String sensorClassName;
	
	public String getSensorId() {
		return sensorId;
	}
	public void setSensorId(String sensorId) {
		this.sensorId = sensorId;
	}
	public String getSensorName() {
		return sensorName;
	}
	public void setSensorName(String sensorName) {
		this.sensorName = sensorName;
	}
	public String getSensorType() {
		return sensorType;
	}
	public void setSensorType(String sensorType) {
		this.sensorType = sensorType;
	}
	public String getSensorJarName() {
		return sensorJarName;
	}
	public void setSensorJarName(String sensorJarName) {
		this.sensorJarName = sensorJarName;
	}
	public String getSensorClassName() {
		return sensorClassName;
	}
	public void setSensorClassName(String sensorClassName) {
		this.sensorClassName = sensorClassName;
	}
}
