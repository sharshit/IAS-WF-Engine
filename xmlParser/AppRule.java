package xmlParser;

import java.util.List;

public class AppRule{
	AppData appInfo;
	List<Rule> rules;
	
	public AppData getAppInfo() {
		return appInfo;
	}
	public void setAppInfo(AppData appInfo) {
		this.appInfo = appInfo;
	}
	public List<Rule> getRule() {
		return rules;
	}
	public List<Rule> setRule(List<Rule> rules) {
		return rules;
	}
	
}
